/* USER CODE BEGIN 0 */
void invio_frame (uint8_t indirizzo, uint8_t comando)
{
	uint8_t frame[4];
	frame[0]= 0x7B; //{
	frame[1]= indirizzo; //indirizzo
	frame[2]= comando; //indirizzo
	frame[3]= 0x7D; //}

	HAL_UART_Transmit(&huart2, frame, 4, HAL_MAX_DELAY);
}

void leggi_frame (uint8_t *comandi)
{
	int i=0;
	uint8_t frame;
	while(1)
	{
		if(HAL_UART_Receive(&huart2, &frame, 1, 0) == HAL_OK)
		{
			comandi[i]=frame;
			if(frame == 0x7D)
			{
				break;
			}
			i++;
		}
	}
}
